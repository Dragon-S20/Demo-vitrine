import React from 'react'
import Button from '@mui/material/Button';
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
import Fade from '@mui/material/Fade';

export default function FadeMenu() {
  const [anchorEl, setAnchorEl] = React.useState(null);
  const open = Boolean(anchorEl);
  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <div>
      <Button
        id="fade-button"
        aria-controls="fade-menu"
        aria-haspopup="true"
        aria-expanded={open ? 'true' : undefined}
        onClick={handleClick}
      >
        Menu
      </Button>
      <Menu
        id="fade-menu"
        MenuListProps={{
          'aria-labelledby': 'fade-button'
        }}
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
        TransitionComponent={Fade}
      >
        <MenuItem onClick={handleClose}>Nouveaut√©s</MenuItem>
        <MenuItem onClick={handleClose}>Femmes</MenuItem>
        <MenuItem onClick={handleClose}>Hommes</MenuItem>
        <MenuItem onClick={handleClose}>Avec des courbes</MenuItem>
        <MenuItem onClick={handleClose}>Soldes</MenuItem>
        <MenuItem onClick={handleClose}>A propos</MenuItem>
        <MenuItem onClick={handleClose}>FAQS</MenuItem>
      </Menu>
    </div>
  );
}
 